#include "Clock.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
std::string Clock::currentDateTime(void)
{
        time_t     now = time(0);
        struct tm  tstruct;
        char       buf[15];
        tstruct = *localtime(&now);
        strftime(buf, sizeof(buf), "%X", &tstruct);
        return buf;
}
